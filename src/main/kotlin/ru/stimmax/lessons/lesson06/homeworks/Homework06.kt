//Задание 2: "Расчет возраста питомца"
//
//Создайте функцию, которая преобразует возраст собаки в "человеческие" годы.
//До 2 лет каждый год собаки равен 10.5 человеческим годам,
//после - каждый год равен 4 человеческим годам.
//Результат распечатай в консоль.

//Если собаке 1 или 2 года, каждый год = 10.5 человеческих лет. Мы не будем брать в расчет меньше 1 года.
//Если собаке больше 2 лет, первые 2 года = по 10.5, остальные = по 4 года

fun task2(ageDog: Int) {
    val ageHuman = if (ageDog <= 2) {
        ageDog * 10.5
    } else {
        2 * 10.5 + (ageDog - 2) * 4
    }
    println("Вашей собаке в человеческих годах: $ageHuman")
}

