//Задание 4: "Расчет бонусных баллов"
//
//Клиенты интернет-магазина получают бонусные баллы за покупки.
//Напишите функцию, которая принимает сумму покупки и печатает
//в консоль количество бонусных баллов: 2 балла за каждые 100 рублей при сумме
//покупки до 1000 рублей и 3 балла за каждые 100 рублей при сумме свыше этого.

fun task4(shop: Int) {
    val bonus = if (shop <= 1000) {
        shop / 100 * 2
    } else {
        1000 / 100 * 2 + (shop - 1000) / 100 * 3
    }
    println("Колличество бонусных баллов: $bonus")
}

